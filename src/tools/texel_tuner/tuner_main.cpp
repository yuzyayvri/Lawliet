struct PositionEntry {
    std::string fen;
    double result; // 1.0 = Win, 0.5 = Draw, 0.0 = Loss
};

struct SparseFeature {
    int32_t index;
    int16_t value;
};

using SparseVector = std::vector<SparseFeature>;

bool parse_epd_line(const std::string& line, std::string& fen, double& result) {
    if (line.empty()) return false;
    if (line.find("1-0") != std::string::npos) result = 1.0;
    else if (line.find("0-1") != std::string::npos) result = 0.0;
    else if (line.find("1/2-1/2") != std::string::npos) result = 0.5;
    else return false;

    // Extract only the first 4 FEN fields (position, side, castling, en-passant).
    // EPD lines may have extra operands (c0, c9, hmvc, etc.) after the FEN
    // that do not belong in a standard FEN string.
    std::istringstream iss(line);
    std::string part;
    fen = "";
    int fields = 0;
    while (iss >> part && fields < 4) {
        fen += part + " ";
        fields++;
    }
    return true;
}

// Recompute mgPst and egPst from scratch using current static PST arrays.
// Must be called after loadParams() when PST or piece-value parameters change,
// because evaluateBoard() reads the pre-computed mgPst/egPst which become stale
// whenever PST or piece-value tables are modified.
void recompute_pst(Board& board) {
    int mg = 0;
    int eg = 0;
    for (int i = 0; i < 12; ++i) {
        uint64_t bb = board.pieceBB[i];
        while (bb) {
            int sq = __builtin_ctzll(bb);
            bb &= bb - 1;
            bool is_white = (i < 6);
            int type = i % 6;
            int idx = is_white ? sq : ((7 - (sq / 8)) * 8 + (sq % 8));
            int m = Board::pieceValuesMidgame[type] + Board::pstMidgame[type][idx];
            int e = Board::pieceValuesEndgame[type] + Board::pstEndgame[type][idx];
            if (is_white) {
                mg += m;
                eg += e;
            } else {
                mg -= m;
                eg -= e;
            }
        }
    }
    board.mgPst = mg;
    board.egPst = eg;
}

