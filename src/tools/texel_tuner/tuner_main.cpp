static bool is_pst_or_value_param(const std::string& name) {
    return (name.rfind("pawnTable", 0) == 0 ||
            name.rfind("knightTable", 0) == 0 ||
            name.rfind("bishopTable", 0) == 0 ||
            name.rfind("rookTable", 0) == 0 ||
            name.rfind("queenTable", 0) == 0 ||
            name.rfind("kingTable", 0) == 0 ||
            name.rfind("pieceValues", 0) == 0);
}

static bool is_king_attack_param(const std::string& name) {
    return (name.rfind("KingZoneAttack", 0) == 0 ||
            name == "KingDangerScaleMg");
}



int main(int argc, char* argv[]) {
    std::cout << "=== Lawliet Texel Tuning Framework ===" << std::endl;
    std::string filename = "zurichess_quiet.epd";
    if (argc > 1) {
        filename = argv[1];
    }

    std::cout << "Loading EPD dataset from: " << filename << std::endl;
    std::ifstream file(filename);
    if (!file) {
        std::cerr << "Error: Dataset file not found." << std::endl;
        return 1;
    }

    std::vector<PositionEntry> dataset;
    std::string line;
    while (std::getline(file, line)) {
        PositionEntry entry;
        if (parse_epd_line(line, entry.fen, entry.result)) {
            dataset.push_back(entry);
        }
    }
    std::cout << "Successfully parsed " << dataset.size() << " valid positions." << std::endl;

    std::vector<ParameterRef> refs = get_parameter_references(g_Params);
    std::cout << "Registered parameters for optimization: " << refs.size() << std::endl;

    // Split Dataset into Train/Validation
    std::mt19937 rng(42);
    std::shuffle(dataset.begin(), dataset.end(), rng);
    size_t val_size = dataset.size() / 10;
    size_t train_size = dataset.size() - val_size;

    std::vector<PositionEntry> train_set(dataset.begin(), dataset.begin() + train_size);
    std::vector<PositionEntry> val_set(dataset.begin() + train_size, dataset.end());

    std::cout << "Dataset split -> Train: " << train_size << " | Validation: " << val_size << std::endl;

    // Feature extraction
    // NOTE: This is single-threaded because the perturbation approach modifies
    // the global g_Params and Board::static arrays. Multi-threaded access would
    // cause data races and incorrect feature deltas.
    std::cout << "Pre-calculating sparse feature matrices..." << std::endl;
    std::vector<SparseVector> train_features(train_size);
    std::vector<double> train_base_scores(train_size);

    Board board;
    Lawliet engine;
    std::vector<ParameterRef> local_refs = get_parameter_references(g_Params);

    for (size_t i = 0; i < train_size; ++i) {
        board.loadFen(train_set[i].fen);

        int base_score = engine.evaluateBoard(board);
        train_base_scores[i] = base_score;

        SparseVector features;
        for (size_t j = 0; j < local_refs.size(); ++j) {
            if (is_king_attack_param(local_refs[j].name)) {
                continue;
            }

            int original_val = *local_refs[j].ptr;
            *local_refs[j].ptr = original_val + 1;

            bool is_cached = is_pst_or_value_param(local_refs[j].name);
            if (is_cached) {
                board.loadParams();
                recompute_pst(board);
            }

            int new_score = engine.evaluateBoard(board);

            *local_refs[j].ptr = original_val;
            if (is_cached) {
                board.loadParams();
                recompute_pst(board);
            }

            int diff = new_score - base_score;
            if (diff != 0) {
                features.push_back({static_cast<int32_t>(j), static_cast<int16_t>(diff)});
            }
        }
        train_features[i] = features;

        if ((i + 1) % 10000 == 0) {
            std::cout << "  Processed " << (i + 1) << " / " << train_size << " training positions..." << std::endl;
        }
    }

    std::cout << "Extracting validation features..." << std::endl;
    std::vector<SparseVector> val_features(val_size);
    std::vector<double> val_base_scores(val_size);

    for (size_t i = 0; i < val_size; ++i) {
        board.loadFen(val_set[i].fen);

        int base_score = engine.evaluateBoard(board);
        val_base_scores[i] = base_score;

        SparseVector features;
        for (size_t j = 0; j < local_refs.size(); ++j) {
            if (is_king_attack_param(local_refs[j].name)) {
                continue;
            }

            int original_val = *local_refs[j].ptr;
            *local_refs[j].ptr = original_val + 1;

            bool is_cached = is_pst_or_value_param(local_refs[j].name);
            if (is_cached) {
                board.loadParams();
                recompute_pst(board);
            }

            int new_score = engine.evaluateBoard(board);

            *local_refs[j].ptr = original_val;
            if (is_cached) {
                board.loadParams();
                recompute_pst(board);
            }

            int diff = new_score - base_score;
            if (diff != 0) {
                features.push_back({static_cast<int32_t>(j), static_cast<int16_t>(diff)});
            }
        }
        val_features[i] = features;

        if ((i + 1) % 10000 == 0) {
            std::cout << "  Processed " << (i + 1) << " / " << val_size << " validation positions..." << std::endl;
        }
    }

    // Initial sigmoid constant scaling factor search (K calibration)
    std::cout << "Calibrating sigmoid scale K..." << std::endl;
    double best_k = 1.13;
    double best_mse = 1e9;
    for (double k = 0.5; k <= 3.0; k += 0.01) {
        double mse = 0.0;
        double c = k * std::log(10.0) / 400.0;
        for (size_t i = 0; i < train_size; ++i) {
            double s = 1.0 / (1.0 + std::exp(-c * train_base_scores[i]));
            double diff = train_set[i].result - s;
            mse += diff * diff;
        }
        mse /= train_size;
        if (mse < best_mse) {
            best_mse = mse;
            best_k = k;
        }
    }
    std::cout << "Optimal calibration: K = " << std::fixed << std::setprecision(4) << best_k << " (Base Train MSE: " << best_mse << ")" << std::endl;

    double C = best_k * std::log(10.0) / 400.0;

    // Optimization Parameter vectors
    size_t num_params = refs.size();
    std::vector<double> theta(num_params, 0.0);
    std::vector<double> m(num_params, 0.0);
    std::vector<double> v(num_params, 0.0);

    // Hyperparameters
    const int epochs = 50;
    const int batch_size = 2048;
    const double base_lr = 0.35;
    const double beta1 = 0.9;
    const double beta2 = 0.999;
    const double epsilon = 1e-8;
    const double l2_lambda = 1e-6;
    const int early_stopping_patience = 5;

    std::cout << "\nStarting optimization loop (mini-batch Adam with cosine annealing)..." << std::endl;
    std::cout << "  Batch size: " << batch_size << " -> ~" << (train_size / batch_size) << " updates per epoch" << std::endl;
    std::cout << "  Learning rate schedule: cosine annealing from " << base_lr << " to 0" << std::endl;
    std::cout << "  Early stopping patience: " << early_stopping_patience << " epochs" << std::endl;
    std::cout << "--------------------------------------------------------" << std::endl;
    std::cout << " Epoch | Train MSE  | Val MSE    |   LR    | Updates" << std::endl;
    std::cout << "--------------------------------------------------------" << std::endl;

    double best_val_mse = 1e9;
    std::vector<double> best_theta(num_params, 0.0);
    int epochs_no_improve = 0;
    int total_updates = 0;

    // Pre-compute training index array for shuffling
    std::vector<size_t> train_indices(train_size);
    for (size_t i = 0; i < train_size; ++i) train_indices[i] = i;

    for (int epoch = 1; epoch <= epochs; ++epoch) {
        // Shuffle training indices for each epoch
        std::shuffle(train_indices.begin(), train_indices.end(), rng);

        // Cosine annealing: learning rate decays from base_lr to 0
        double cosine_progress = static_cast<double>(epoch - 1) / epochs;
        double lr = base_lr * 0.5 * (1.0 + std::cos(cosine_progress * M_PI));

        double epoch_train_mse = 0.0;
        size_t batches_in_epoch = 0;

        // Mini-batch loop
        for (size_t batch_start = 0; batch_start < train_size; batch_start += batch_size) {
            size_t batch_end = std::min(batch_start + batch_size, train_size);
            size_t batch_actual = batch_end - batch_start;

            // Compute gradient over this mini-batch
            std::vector<double> grad(num_params, 0.0);
            double batch_mse = 0.0;

            for (size_t b = batch_start; b < batch_end; ++b) {
                size_t idx = train_indices[b];
                double score = train_base_scores[idx];
                for (const auto& f : train_features[idx]) {
                    score += theta[f.index] * f.value;
                }

                double s = 1.0 / (1.0 + std::exp(-C * score));
                double diff = train_set[idx].result - s;
                batch_mse += diff * diff;

                double derivative = -2.0 * C * diff * s * (1.0 - s);
                for (const auto& f : train_features[idx]) {
                    grad[f.index] += derivative * f.value;
                }
            }

            batch_mse /= batch_actual;
            epoch_train_mse += batch_mse * batch_actual;

            // Average the gradient over the batch and add L2 regularization
            for (size_t j = 0; j < num_params; ++j) {
                grad[j] = (grad[j] / batch_actual) + l2_lambda * theta[j];
            }

            // Adam update
            total_updates++;
            for (size_t j = 0; j < num_params; ++j) {
                m[j] = beta1 * m[j] + (1.0 - beta1) * grad[j];
                v[j] = beta2 * v[j] + (1.0 - beta2) * (grad[j] * grad[j]);

                double m_hat = m[j] / (1.0 - std::pow(beta1, total_updates));
                double v_hat = v[j] / (1.0 - std::pow(beta2, total_updates));

                theta[j] -= lr * m_hat / (std::sqrt(v_hat) + epsilon);
            }

            batches_in_epoch++;
        }

        epoch_train_mse /= train_size;

        // Evaluate on validation set
        double val_mse = 0.0;
        for (size_t i = 0; i < val_size; ++i) {
            double score = val_base_scores[i];
            for (const auto& f : val_features[i]) {
                score += theta[f.index] * f.value;
            }
            double s = 1.0 / (1.0 + std::exp(-C * score));
            double diff = val_set[i].result - s;
            val_mse += diff * diff;
        }
        val_mse /= val_size;

        // Track and save the best parameter epoch
        if (val_mse < best_val_mse) {
            best_val_mse = val_mse;
            best_theta = theta;
            epochs_no_improve = 0;
        } else {
            epochs_no_improve++;
        }

        std::cout << " " << std::setw(5) << epoch << " | "
        << std::setw(10) << std::fixed << std::setprecision(6) << epoch_train_mse << " | "
        << std::setw(10) << val_mse << " | "
        << std::setw(6) << std::setprecision(4) << lr << " | "
        << std::setw(7) << total_updates << std::endl;

        if (epochs_no_improve >= early_stopping_patience) {
            std::cout << "  Early stopping triggered (no improvement for " << early_stopping_patience << " epochs)." << std::endl;
            break;
        }
    }

    std::cout << "--------------------------------------------------------" << std::endl;
    std::cout << "Total parameter updates: " << total_updates << std::endl;
    std::cout << "Optimal epoch found with Validation MSE: " << best_val_mse << std::endl;

    // Apply best parameters back to the active configuration
    for (size_t j = 0; j < num_params; ++j) {
        *refs[j].ptr = refs[j].default_value + static_cast<int>(std::round(best_theta[j]));
    }

    std::cout << "Writing optimized parameters directly to source file..." << std::endl;
    save_parameters_to_file("src/core/parameters.cpp", g_Params);
    std::cout << "Texel Tuning Completed Successfully! Output written to parameters.cpp." << std::endl;

    return 0;
}
